package com.donat.donchess.dto.chessGame;

public class CoordinateDto {

    private Long fromX;

    private Long fromY;

    private Long toX;

    private Long toY;

    public Long getFromX() {
        return fromX;
    }

    public void setFromX(Long fromX) {
        this.fromX = fromX;
    }

    public Long getFromY() {
        return fromY;
    }

    public void setFromY(Long fromY) {
        this.fromY = fromY;
    }

    public Long getToX() {
        return toX;
    }

    public void setToX(Long toX) {
        this.toX = toX;
    }

    public Long getToY() {
        return toY;
    }

    public void setToY(Long toY) {
        this.toY = toY;
    }
}
